// There are 7 data types

// Number, String, Boolean, Null, Undefined, Object, Symbol

let num = 10; // Number
print(typeof num); // Output: number

let str = "Hello"; // String
print(typeof str); // Output: string

let bool = true; // Boolean
print(typeof bool); // Output: boolean

let n = null; // Null
print(typeof n); // Output: object

let u; // Undefined
print(typeof u); // Output: undefined

let obj = { name: "John", age: 30 }; // Object
print(typeof obj); // Output: object

let sym = Symbol("id"); // Symbol
print(typeof sym); // Output: symbol

