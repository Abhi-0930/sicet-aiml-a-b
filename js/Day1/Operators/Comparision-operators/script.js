num1 = 10
num2 = 20

console.log(num1 == num2); // Output: false

console.log(num1 != num2); // Output: true

console.log(num1 > num2); // Output: false

console.log(num1 < num2); // Output: true

console.log(num1 >= num2); // Output: false

console.log(num1 <= num2); // Output: true

console.log("1" == 1); // Output: true

console.log("1" === 1); // Output: false